    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        
        if (evt.getClickCount() == 2) {
            int r = jTable1.getSelectedRow();

            if (r == -1) {
                JOptionPane.showMessageDialog(this, "Please select a branch", "Warning", JOptionPane.WARNING_MESSAGE);
            } else {

                if (invoice != null) {
                    String sid = jTable1.getValueAt(r, 0).toString();
                    String sname = jTable1.getValueAt(r, 1).toString();
                    String smobile = jTable1.getValueAt(r, 2).toString();
                    String city = jTable1.getValueAt(r, 3).toString();

                    invoice.jLabel2.setText(sid);
                    invoice.jLabel4.setText(sname);
                    invoice.jLabel6.setText(smobile);
                    invoice.jLabel8.setText(city);

                    invoice.jButton1.setText("Customer");
                    invoice.jButton1.setEnabled(false);

                    this.dispose();
                }
                
                if (quotation != null) {
                    String sid = jTable1.getValueAt(r, 0).toString();
                    String sname = jTable1.getValueAt(r, 1).toString();
                    String smobile = jTable1.getValueAt(r, 2).toString();
                    
                    quotation.jLabel23.setText(sid);
                    quotation.jLabel4.setText(sname);
                    quotation.jLabel5.setText(smobile);
                    
                    quotation.jButton1.setText("customer");
                    quotation.jButton1.setEnabled(false);
                    
                    this.dispose();
                    
                }

            }
        }
    }//GEN-LAST:event_jTable1MouseClicked

