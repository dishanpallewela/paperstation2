    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String name = jTextField1.getText();
        String mobile = jTextField2.getText();
        String city = jComboBox1.getSelectedItem().toString();

        if (name.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter name", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (!Pattern.compile("07[01245678][0-9]{7}").matcher(mobile).matches()) {
            JOptionPane.showMessageDialog(this, "Please enter mobile number", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (city.equals("Select")) {
            JOptionPane.showMessageDialog(this, "Please select city", "Warning", JOptionPane.WARNING_MESSAGE);
        } else {
            try {
                ResultSet rs1 = MySQL.search("SELECT `id` FROM `city` WHERE `name`='" + city + "'");
                rs1.next();

                String city_id = rs1.getString("id");

                ResultSet cus = MySQL.search("SELECT * FROM `customer` WHERE `name` = '" + name + "' OR `contact_number` = '" + mobile + "' ");

                if (cus.next()) {
                    JOptionPane.showMessageDialog(this, "This Customer Already added ", "Error", JOptionPane.INFORMATION_MESSAGE);
                } else {
                    MySQL.iud("INSERT INTO `customer`(`name`,`contact_number`,`city_id`) VALUES('" + name + "','" + mobile + "'," + Integer.parseInt(city_id) + ")");

                    resetFields();
                    loadCustomers();

                    JOptionPane.showMessageDialog(this, "New Customer Saved", "Success", JOptionPane.INFORMATION_MESSAGE);
                }

            } catch (Exception e) {
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

