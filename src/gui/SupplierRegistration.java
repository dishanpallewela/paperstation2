    public void loadSupplier() {

        try {
            ResultSet rs = MySQL.search("SELECT `supplier`.`id` AS `sup_id` , `supplier`.`name` AS `supname`, `supplier`.`contact_number`,`supplier`.`address_line1`,`supplier`.`address_line2`,`city`.`name` AS `cityname` FROM `supplier` INNER JOIN `city` ON `city`.`id`=`supplier`.`city_id` ORDER BY `supplier`.`id` ASC");

            DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
            dtm.setRowCount(0);

            while (rs.next()) {
                Vector v = new Vector();
                v.add(rs.getString("sup_id"));
                v.add(rs.getString("supname"));
                v.add(rs.getString("contact_number"));
                v.add(rs.getString("address_line1"));
                v.add(rs.getString("address_line2"));
                v.add(rs.getString("cityname"));
                
                
                dtm.addRow(v);
            }
        } catch (Exception e) {
            e.printStackTrace();
        }

    }

    public void loadCities() {

        try {

            ResultSet rs = MySQL.search("SELECT * FROM `city`");

            Vector v = new Vector();
            v.add("Select");

            while (rs.next()) {
                v.add(rs.getString("name"));
            }

            DefaultComboBoxModel dcm = new DefaultComboBoxModel(v);
            jComboBox1.setModel(dcm);

        } catch (Exception e) {
            e.printStackTrace();
        }

    }

